package Condition;

public class If_Demo {

	public static void main(String[] args) {
		
		System.out.println("This is the beginning of IF statement");
		int x = 90;
		if(x<100);
		{
		System.out.println("The first condition is True: "+x);
		}
		
		
		if(x<50);
		{
		System.out.println("The second condition is True: "+x);
		}
 
		System.out.println("This is the end of IF statement");

	}

}
