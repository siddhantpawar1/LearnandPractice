package Loops_Demo;

public class Loop_Demo1 {

	public static void main(String[] args) {
		
		System.out.println("The loop started from here!!");
				
		for(int num=1; num<=20; num++)
		
		{
			System.out.println("Number values are: "+num);
			
			System.out.println("The looping ends here!");
		}

	}

}
